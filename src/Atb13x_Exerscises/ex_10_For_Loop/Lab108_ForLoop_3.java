package Atb13x_Exerscises.ex_10_For_Loop;

public class Lab108_ForLoop_3 {
    public static void main(String[] args) {

        for (int i = -1; i < -10 ; i--) {
            System.out.println(i);
        }


        for (int i = -10; i < -1; i++) {
            System.out.println(i);
        }


    }
}
