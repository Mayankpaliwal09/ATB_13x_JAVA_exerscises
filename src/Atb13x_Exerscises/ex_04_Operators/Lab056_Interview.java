package Atb13x_Exerscises.ex_04_Operators;

public class Lab056_Interview {
    public static void main(String[] args) {
        short s = 10;
        char c = 'A';
        String s1 = "A";
        System.out.println(c+s); //c = 65 , s= 1- -->65+10 =75
        System.out.println(c);  //A
        System.out.println(c+c); // c= 65 + c= 65 ==> 130
        System.out.println(c+s1);//AA
    }
}